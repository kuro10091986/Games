# 6. 出題ロジック
# ==========================================
def generate_q(subj):
    s = st.session_state.study_states[subj]
    db = DATABASE[subj]
    s["step"] += 1
    
    is_rv = (s["wrong_pool"] and s["step"] >= s["next_rv"])
    if is_rv:
        idx = random.choice(s["wrong_pool"])
        s["next_rv"] = s["step"] + random.randint(3, 5)
    elif s["rem_ids"]:
        idx = random.choice(s["rem_ids"])
    elif s["wrong_pool"]:
        idx = random.choice(s["wrong_pool"])
    else:
        s["cur_id"] = -99
        return

    data = db[idx]
    if subj == "英単語":
        mode = random.randint(0, 1) # 0:JP->EN, 1:EN->JP
        q_txt = f"「{data[0]}」は英語で？" if mode == 0 else f"「{data[1]}」の意味は？"
        a = data[1] if mode == 0 else data[0]
        pool = [d[1] if mode == 0 else d[0] for d in db]
    else:
        q_txt = data.get("q") or data.get("question")
        a = data.get("a") or data.get("answer")
        pool = [d.get("a") or d.get("answer") for d in db]

    opts = [a] + random.sample([x for x in pool if x != a], 3)
    random.shuffle(opts)
    
    s.update({"cur_id": idx, "q_txt": q_txt, "q_ans": a, "q_opts": opts, "ans": False})

